            Los primeros bordadores de La Paz abrieron sus talleres   en la zona de Putu Putu, ahora conocida como Miraflores. Después,  se trasladaron a la calle Chirinos, luego a la zona de Gran Poder (Max Paredes, Buenos Aires e Illampu). Para finalmente   establecerse en la avenida Kollasuyo y la calle Los Andes. <br>
            La descripción histórica fue ayer  realizada por    Natalio Quisbert, de Bordadores Bolívar, en  el  acto de entrega de reconocimiento a los artistas bordadores de la danza morenada. La ceremonia se realizó en el Ministerio de Culturas, donde además se efectuó la presentación del  registro de artistas a la agrupación Sajama Fusión y una exposición de trajes de morenos. <br>
            Las familias Gutiérrez e Ibáñez fueron representantes del bordado de  El Alto. Según Quisbert, los antiguos bordadores trabajaban encadenados,  manualmente con bastidores. La realización de un traje de moreno  lleva aproximadamente dos meses y quienes se dedicaban a esto se la pasaban bordando día y noche.<br>
            "La responsabilidad y la honestidad fueron dos valores que nos dejaron nuestros antepasados. Por ejemplo, si hay una entrada se tiene que cumplir con los trajes hasta  esa fecha” añadió Quisbert. <br>
            Existen dos clases de bordadores, los que se educaron por generación, con la transmisión de conocimiento de padres a hijos y otros que fueron operarios que con el correr del tiempo se convirtieron en grandes bordadores, explicó Quisbert.<br>
            En predios del Palacio Chico del Ministerio de Culturas, los bordadores organizaron una exposición de diferentes trajes de morenada, tanto de chinas como de morenos. El objetivo de la muestra fue  compartir sus obras y reivindicar el oficio del bordador como un arte. <br>
            "Las autoridades nos motivaron para exhibir  parte  del   trabajo de los bordadores. Esperamos posteriormente realizar otras muestras  con el apoyo de dicha institución”,  acotó.<br>
            Se discutieron diferentes teorías del origen de la morenada. Lo cierto es que esta danza es de origen boliviano. "Otros países como Chile y Perú también discuten sobre el origen;  ellos dicen que es andino, y por tal motivo es que se ha estado incluyendo los colores de la bandera boliviana a los trajes de morenos para dar a conocer que nosotros somos los pioneros” dijo Quisbert.<br>
            Dentro de la Asociación Mixta de Artistas Bordadores Autodidactas (AMABA) se reconoció a bordadores que trabajaron por más de 60 años, los Socios Bordadores de Danza de la Morenada de la tercera edad,  entre ellos Tomás Balboa, Rufino Chuquimia y Alfredo Quisbert Machicado.<br>
            Luego se entregaron las credenciales del registro plurinacional de artistas a los bordadores de la danza morenada y al grupo Sajama Fusión. "Son 15 años que estamos en este ámbito, tiempo en el cual conocimos varias personas y lugares en distintas presentaciones en el exterior.<br>
            Siempre ha sido un honor sacar el nombre de Bolivia adelante” dijo Franklin Montecinos, integrante de Sajama Fusión.<br>
            "Con la credencial que se les ha entregado a los bordadores, ellos pueden garantizar a sus clientes que su trabajo artístico está certificado”,  expresó Javier Escalier, jefe de la Unidad de Coordinación del Consejo Departamental de Cultura.<br>
